#include "fdf.h"

int	rgba(void)
{
	int r;
	int	g;
	int	b;
	int	a;

	r = 0xFF000000;
	g = 0x00FF0000;
	b = 0x0000FF00;
	a = 0x000000FF;
	return (r << 24 | g << 16 | b << 8 | a);
}

